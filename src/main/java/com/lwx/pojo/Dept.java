package com.lwx.pojo;

public class Dept {
	private int dept_id;
	private String dept_name;
	private String dept_leader;
	private int dept_num;
	public int getDept_num() {
		return dept_num;
	}
	public void setDept_num(int dept_num) {
		this.dept_num = dept_num;
	}
	public int getDept_id() {
		return dept_id;
	}
	public void setDept_id(int dept_id) {
		this.dept_id = dept_id;
	}
	public String getDept_name() {
		return dept_name;
	}
	public void setDept_name(String dept_name) {
		this.dept_name = dept_name;
	}
	public String getDept_leader() {
		return dept_leader;
	}
	public void setDept_leader(String dept_leader) {
		this.dept_leader = dept_leader;
	}
	
	
	
}
